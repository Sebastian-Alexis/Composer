from flask import Flask, render_template, request, jsonify
from scraper import scrape_wikipedia_info
from togetherapi import generate_email

app = Flask(__name__)

@app.route("/")
def index():
    return render_template("index.html")

@app.route("/scrape", methods=["POST"])
def scrape():
    data = request.json
    wikipedia_url = data.get("wikipedia_url", "")
    result = scrape_wikipedia_info(wikipedia_url)
    return jsonify(result)

@app.route("/generate-email", methods=["POST"])
def generate_email_route():
    data = request.json
    template = data.get("template", "")
    company_summary = data.get("company_summary", "")
    email = generate_email(template, company_summary)
    return jsonify({"email": email})

if __name__ == "__main__":
    app.run(host="0.0.0.0", port=1922, debug=True)
    app.run(debug=True)






















































































































































































































































































































































































































































































































































































































































































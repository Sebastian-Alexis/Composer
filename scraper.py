import wikipediaapi

def scrape_wikipedia_info(wikipedia_url):
    try:
        # Extract the page title from the URL
        if "wikipedia.org/wiki/" not in wikipedia_url:
            return {"error": "Invalid Wikipedia URL."}
        
        page_title = wikipedia_url.split("/wiki/")[-1].replace("_", " ")

        # Initialize Wikipedia API
        user_agent = "ComposerApp/1.0 (https://yourwebsite.com; your-email@example.com)"
        wiki = wikipediaapi.Wikipedia(
            language="en",
            user_agent=user_agent
        )
        
        page = wiki.page(page_title)

        # Check if the page exists
        if not page.exists():
            return {"error": "Wikipedia page not found."}

        # Return the title and summary
        return {
            "summary": page.summary
        }
    except Exception as e:
        return {"error": str(e)}



















































































































































































































































































































































































































































































































































































































































































